SELECT 
    NOMECLIENTE,
    PLACA,
    TIPOVEICULO,
    'NO ESTACIONAMENTO' AS STATUS
FROM
    ENTRADA
WHERE
    STATUSVEICULO = 'ABERTO' 
UNION ALL SELECT 
    E.NOMECLIENTE, E.PLACA, E.TIPOVEICULO, 'SAIU' AS STATUS
FROM
    SAIDA S
	INNER JOIN ENTRADA E 
    ON S.IDENTRADA = E.IDENTRADA;
    
